package apryraz.bworld;

/**
 * Class to represent a position or coordinate (x,y).
 */
public class Position {
	/**
	 * x and y coordinates on the x and y axis respectively.
	 **/
	public int x, y;


	/**
	 * Class constructor.
	 *
	 * @param a x coordinate.
	 * @param b y coordinate.
	 */
	public Position(int a, int b) {
		x = a;
		y = b;
	}
}
